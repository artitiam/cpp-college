#include <iostream>
#include <cstdlib>
#include <ctime>
using namespace std;

int main()
{
  int size = 10;    // Размер исходного массива — 10 элементов
  int halfSize = 5; // Размер каждого из двух новых массивов — 5 элементов

  int source[size];         // Исходный массив из 10 целых чисел
  int firsthalf[halfSize];  // Массив для хранения первых 5 элементов
  int secondhalf[halfSize]; // Массив для хранения последних 5 элементов

  srand(time(nullptr)); // Инициализация генератора случайных чисел текущим временем

  cout << "Исходный массив: ";   // Вывод заголовка перед элементами исходного массива
  for (int i = 0; i < size; i++) // Цикл для заполнения и отображения исходного массива
  {
    source[i] = rand() % 201 - 100; // Генерация случайного числа в диапазоне от -100 до 100 включительно
    cout << source[i] << " ";       // Вывод текущего элемента исходного массива
  }
  cout << "\n"; // Переход на новую строку после вывода всего массива

  // Копирование первых 5 элементов из исходного массива в первый новый массив
  for (int i = 0; i < halfSize; i++)
  {
    firsthalf[i] = source[i]; // Присваивание i-го элемента исходного массива i-му элементу firsthalf
  }

  // Копирование последних 5 элементов (с 5-го по 9-й) в второй новый массив
  for (int i = 0; i < halfSize; i++)
  {
    secondhalf[i] = source[i + halfSize]; // Элемент с индексом i+5 из source копируется в secondhalf[i]
  }

  cout << "Первые 5 элементов: "; // Заголовок для вывода первой половины
  for (int i = 0; i < halfSize; i++)
  {
    cout << firsthalf[i] << " "; // Вывод элементов первого нового массива
  }
  cout << "\n"; // Переход на новую строку

  cout << "Последние 5 элементов: "; // Заголовок для вывода второй половины
  for (int i = 0; i < halfSize; i++)
  {
    cout << secondhalf[i] << " "; // Вывод элементов второго нового массива
  }
  cout << "\n"; // Завершение вывода с новой строки

  return 0;
}