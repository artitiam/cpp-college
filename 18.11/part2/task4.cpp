#include <iostream>
#include <vector>
using namespace std;

int main()
{
  const int months = 12; // количество месяцев в году

  double depositEuros; // Переменная для хранения начальной суммы депозита в евро
  cout << "Введите сумму депозита в евро: ";
  cin >> depositEuros;

  double euroToDollar[months];  // Массив курсов евро к доллару (на самом деле: сколько долларов за 1 евро)
  double interestEuros[months]; // Массив начисленных процентов за каждый месяц в евро

  cout << "Введите курс евро к доллару и начисленные проценты (в евро) за каждый месяц:\n";
  for (int i = 0; i < months; i++) // Цикл для ввода данных по каждому месяцу
  {
    cout << "Месяц " << (i + 1) << ":\n";
    cout << "  Курс (1 евро = ? долларов): ";
    cin >> euroToDollar[i]; // Пользователь вводит, сколько долларов даёт 1 евро в этом месяце
    cout << "  Начисленные проценты (в евро): ";
    cin >> interestEuros[i]; // Пользователь вводит сумму начисленных процентов в евро за месяц
  }

  int month; // Переменная для хранения номера месяца, запрошенного пользователем
  cout << "Введите номер месяца (1–12), чтобы узнать допустимую сумму к обналичиванию: ";
  cin >> month;

  // Проверка корректности ввода месяца
  if (month < 1 || month > 12)
  {
    cout << "Некорректный номер месяца. Допустимы значения от 1 до 12.\n";
    return 0; // Завершаем программу при ошибке
  }

  // Индекс в массивах на 1 меньше, чем номер месяца (т.к. индексы с 0)
  int idx = month - 1;

  // Переводим начисленные проценты из евро в доллары по курсу этого месяца
  float interestDollars = interestEuros[idx] * euroToDollar[idx];

  cout << "\n--- Результат для месяца " << month << " ---\n";
  cout << "Начисленные проценты в евро: " << interestEuros[idx] << " €\n";
  cout << "Курс: 1 € = " << euroToDollar[idx] << " $\n";
  cout << "Начисленные проценты в долларах: " << interestDollars << " $\n";

  // Проверяем, достигли ли начисления порога в 500 долларов
  if (interestDollars >= 500.0)
  {
    // Если да — можно обналичить до 50% от этой суммы
    double withdrawable = interestDollars * 0.5;
    cout << "Начисленная сумма >= 500 $, поэтому можно обналичить до 50%.\n";
    cout << "Допустимая сумма к обналичиванию: " << withdrawable << " $\n";
  }
  else
  {
    // Если нет — обналичить ничего нельзя (по условию задачи)
    cout << "Начисленная сумма < 500 $. Обналичивание не разрешено.\n";
    cout << "Допустимая сумма к обналичиванию: 0 $\n";
  }

  return 0;
}