#include <iostream>
#include <vector>
#include <cstdlib>    
#include <ctime> 
using namespace std;

int main()
{
  cout << "Введите минимум диапазона: "; // Запрашивает у пользователя минимальное значение диапазона
  int min;                               // Объявляет переменную для хранения минимального значения
  cin >> min;                            // Считывает введённое пользователем значение в переменную min

  cout << "Введите максимум диапазона: "; // Запрашивает у пользователя максимальное значение диапазона
  int max;                                // Объявляет переменную для хранения максимального значения
  cin >> max;                             // Считывает введённое пользователем значение в переменную max

  cout << "Введите пороговое значение: "; // Запрашивает у пользователя пороговое значение
  int threshold;                          // Объявляет переменную для хранения порога
  cin >> threshold;                       // Считывает пороговое значение от пользователя

  int arr[10]; // Объявляет статический массив из 10 целых чисел

  int sum = 0;                        // Инициализирует переменную для накопления суммы подходящих элементов нулём
  srand(time(nullptr));               // Инициализирует генератор случайных чисел текущим временем
  cout << "Массив: ";                 // Выводит заголовок перед отображением элементов массива
  for (int i = 0; i < size(arr); i++) // Цикл по всем элементам массива (от 0 до 9)
  {
    arr[i] = min + rand() % (max - min + 1); // Генерирует случайное число в диапазоне [min, max] и записывает в массив
    if (threshold > arr[i])                  // Проверяет, меньше ли текущий элемент порогового значения
    {
      sum += arr[i]; // Если да — добавляет элемент к общей сумме
    }

    cout << arr[i] << " "; // Выводит текущий элемент массива с пробелом
  }
  cout << "\n";                                                                 // Переход на новую строку после вывода массива
  cout << "Сумма значений массива меньше " << threshold << ": " << sum << endl; // Выводит итоговую сумму
}