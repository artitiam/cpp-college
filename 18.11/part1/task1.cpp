#include <iostream>  
#include <vector>    
#include <cstdlib>   
#include <ctime>    
using namespace std; 

int main() 
{
  srand(time(nullptr)); // Инициализирует генератор случайных чисел текущим временем, чтобы при каждом запуске программы генерировались разные числа

  int arr[10]; // Объявляет массив из 10 целых чисел

  cout << "Массив: ";          // Выводит заголовок перед отображением элементов массива
  for (int i = 0; i < 10; i++) // Цикл для заполнения и вывода всех 10 элементов массива
  {
    arr[i] = rand() % 201 - 100; // Генерирует случайное число в диапазоне [-100, 100]: rand() % 201 даёт число от 0 до 200, затем вычитаем 100 → [-100, 100]
    cout << arr[i] << " ";       // Выводит текущий элемент массива с пробелом
  }
  cout << "\n"; // Переход на новую строку после вывода всего массива

  int max, min; // Объявляет переменные для хранения максимального и минимального значений
  max = arr[0]; // Предполагаем, что первый элемент — максимальный
  min = arr[0]; // Предполагаем, что первый элемент — минимальный

  // Цикл начинается со второго элемента (индекс 1), так как первый уже взят за начальное значение
  for (int i = 1; i < size(arr); i++) 
  {
    if (max < arr[i]) // Если текущий элемент больше текущего максимума
    {
      max = arr[i]; // Обновляем максимум
    }
    if (min > arr[i]) // Если текущий элемент меньше текущего минимума
    {
      min = arr[i]; // Обновляем минимум
    }
  }

  // Вывод результатов
  cout << "Минимальное число массива: " << min << "\n"
       << "Максимальное число массива: " << max << endl;

  return 0; 
}