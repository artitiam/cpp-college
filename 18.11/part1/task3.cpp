#include <iostream>
#include <vector>
using namespace std;

int main()
{
  int size = 12; // Задаёт size, равную 12 — количество месяцев в году

  cout << "Введите прибыль за 12 месяцев: "; // Запрашивает у пользователя ввод прибыли за каждый месяц
  int profit[size];                          // Объявляет массив из 12 целых чисел для хранения прибыли по месяцам
  for (int i = 0; i < size; i++)             // Цикл для ввода 12 значений прибыли
  {
    cin >> profit[i]; // Считывает прибыль за i-й месяц и сохраняет в массив
  }

  cout << "Введите диапазон в котором хотите найти месяц с максимальной и минимальной прибылью: "; // Запрашивает у пользователя диапазон месяцев (включительно)
  int minRange, maxRange;                                                                          // Объявляет переменные для хранения начального и конечного номеров месяцев диапазона
  cin >> minRange >> maxRange;                                                                     // Считывает два целых числа — начало и конец диапазона (в терминах номеров месяцев: 1–12)

  int minPro = profit[minRange - 1]; // Инициализирует минимальную прибыль значением из первого месяца указанного диапазона (минус 1 — переход к индексу массива)
  int maxPro = profit[minRange - 1]; // Инициализирует максимальную прибыль тем же первым значением

  for (int i = minRange - 1; i <= maxRange - 1; i++) // Цикл по индексам массива, соответствующим указанному диапазону месяцев
  {
    if (maxPro < profit[i]) // Если текущая прибыль больше текущего максимума
    {
      maxPro = profit[i]; // Обновляет максимальную прибыль
    }
    if (minPro > profit[i]) // Если текущая прибыль меньше текущего минимума
    {
      minPro = profit[i]; // Обновляет минимальную прибыль
    }
  }

  cout << "Минимальная прибыль диапазона: " << minPro << "\n"   // Выводит найденную минимальную прибыль
       << "Максимальная прибыль диапазона: " << maxPro << endl; // Выводит найденную максимальную прибыль
  
  return 0;
}