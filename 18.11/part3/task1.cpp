#include <iostream>
#include <algorithm>
using namespace std;

int main()
{
  const int size = 5; // Количество записей в справочнике


  long int mobile[size] = {
      79161234567, 
      79262345678, 
      79033456789, 
      79854567890,
      79655678901  
  };

  long int home[size] = {
      74951112233, 
      78122223344, 
      73433334455,
      73834445566,
      73915556677  
  };

  int choice; // Переменная для выбора пункта меню

  while (true) // Меню
  {
    cout << "\n=== МЕНЮ СПРАВОЧНИКА ===\n";
    cout << "1. Отсортировать по номерам мобильных\n";
    cout << "2. Отсортировать по домашним номерам\n";
    cout << "3. Вывести данные пользователей\n";
    cout << "4. Выход\n";
    cout << "Выберите действие (1–4): ";
    cin >> choice;

    if (choice == 4)
    {
      cout << "Выход из программы.\n";
      break;
    }

    if (choice == 1)
    {
      // Сортировка по мобильным номерам с синхронной перестановкой домашних
      for (int i = 0; i < size - 1; i++)
      {
        for (int j = i + 1; j < size; j++)
        {
          if (mobile[i] > mobile[j])
          {
            swap(mobile[i], mobile[j]);
            swap(home[i], home[j]); // Сохраняем связь между записями
          }
        }
      }
      cout << "Справочник отсортирован по мобильным номерам.\n";
    }
    else if (choice == 2)
    {
      // Сортировка по домашним номерам с синхронной перестановкой мобильных
      for (int i = 0; i < size - 1; i++)
      {
        for (int j = i + 1; j < size; j++)
        {
          if (home[i] > home[j])
          {
            swap(home[i], home[j]);
            swap(mobile[i], mobile[j]); // Сохраняем связь между записями
          }
        }
      }
      cout << "Справочник отсортирован по домашним номерам.\n";
    }
    else if (choice == 3)
    {
      // Вывод данных в читаемом виде (с форматированием как +7 XXX XXX-XX-XX)
      cout << "\nДанные пользователей\n";
      cout << "Мобильный номер      Домашний номер\n";
      for (int i = 0; i < size; i++)
      {
        // Форматируем мобильный: +7 XXX XXX-XX-XX
        cout << "+7 "
             << mobile[i] / 10000000 % 1000 << " "
             << mobile[i] / 10000 % 1000 << "-"
             << mobile[i] / 100 % 100 << "-"
             << mobile[i] % 100;

        cout << "    ";

        // Форматируем домашний: +7 XXX XXX-XX-XX
        cout << "+7 "
             << home[i] / 10000000 % 1000 << " "
             << home[i] / 10000 % 1000 << "-"
             << home[i] / 100 % 100 << "-"
             << home[i] % 100 << "\n";
      }
    }
    else
    {
      cout << "Некорректный выбор. Введите число от 1 до 4.\n";
    }
  }

  return 0;
}