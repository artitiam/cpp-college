#include <iostream>
using namespace std;

int main()
{
  const int rows = 3;  // Количество строк двумерного массива
  const int cols = 4;  // Количество столбцов двумерного массива
  int arr[rows][cols]; // Объявление двумерного массива

  int startValue; // Переменная для хранения начального числа от пользователя
  cout << "Введите начальное число: ";
  cin >> startValue;

  long long currentValue = startValue; // Текущее значение, которое будет записываться в массив, используем long long, чтобы избежать переполнения при умножении на 2

  // Заполняем массив построчно, слева направо, сверху вниз
  for (int i = 0; i < rows; i++) // Цикл по строкам
  {
    for (int j = 0; j < cols; j++) // Цикл по столбцам
    {
      arr[i][j] = static_cast<int>(currentValue); // Присваиваем текущее значение элементу массива

      currentValue *= 2; // Умножаем на 2 для следующего элемента
    }
  }

  // Выводим заполненный массив на экран
  cout << "\nСформированный массив:\n";
  for (int i = 0; i < rows; i++) // Цикл по строкам для вывода
  {
    for (int j = 0; j < cols; j++) // Цикл по столбцам для вывода
    {
      cout << arr[i][j] << "\t"; // Выводим элемент с табуляцией для выравнивания
    }
    cout << "\n"; // Переход на новую строку после каждой строки массива
  }

  return 0;
}