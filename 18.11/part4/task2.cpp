#include <iostream>
using namespace std;

int main()
{
  const int rows = 3;                                 // Количество строк двумерного массива
  const int cols = 4;                                 // Количество столбцов двумерного массива
  int arr[rows][cols];                                // Объявление двумерного целочисленного массива

  int startValue;                                     // Переменная для хранения начального числа, введённого пользователем
  cout << "Введите начальное число: ";
  cin >> startValue;

  int currentValue = startValue;                      // Текущее значение, которое будет записываться в массив

  // Заполняем массив последовательно: по строкам, слева направо
  for (int i = 0; i < rows; i++)                      // Цикл по строкам
  {
    for (int j = 0; j < cols; j++)                    // Цикл по столбцам
    {
      arr[i][j] = currentValue;                       // Присваиваем текущее значение элементу массива
      currentValue++;                                 // Увеличиваем значение на 1 для следующего элемента
    }
  }

  // Выводим заполненный массив на экран
  cout << "\nСформированный массив:\n";
  for (int i = 0; i < rows; i++)                      // Цикл по строкам для вывода
  {
    for (int j = 0; j < cols; j++)                    // Цикл по столбцам для вывода
    {
      cout << arr[i][j] << "\t";                      // Выводим элемент с табуляцией для выравнивания
    }
    cout << "\n";                                     // Переход на новую строку после окончания строки массива
  }

  return 0;
}